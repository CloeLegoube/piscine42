#include <unistd.h>

void	ft_generic(void)
{
	int		i;
	char	*str;

	i = 0;
	str = "Tu tu tu tu ; Tu tu tu tu";
	while (str[i])
		write(1, &str[i++], 1);
	write(1, "\n", 1);
}
